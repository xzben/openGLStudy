SkyBox::SkyBox()
{
	this->m_mesh = new Mesh();
	this->m_material = new Material(BuiltInShader::SKY_BOX);
}

SkyBox::~SkyBox()
{

}

void SkyBox::render(RenderData* render)
{
	this->m_material->setDepthFunc(DepthFunc::LEQUAL);
	this->doDraw(render, this->m_mesh, this->m_material);
}

bool SkyBox::init(std::string faces[6])
{
	this->m_mesh->setup(skyboxVertices, 36, nullptr, 0, VerticleMember::POS);

	TextureCube* cube = new TextureCube();
	cube->init(faces);
	this->m_material->setMainTexture(cube);

	return true;
}

